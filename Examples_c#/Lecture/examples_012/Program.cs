void PrintArray(int[,] pic)
{
    for (int rows = 0; rows < pic.GetLength(0); rows++)
    {
        for (int cols = 0; cols < pic.GetLength(1); cols++)
        {
            if (pic[rows, cols] == 0) System.Console.Write($" ");
            //System.Console.Write(pic[rows,cols]);
            else System.Console.Write($"#");
        }
        System.Console.WriteLine();
    }
}

void FillImage(int row, int col)
{
    if (pic[row, col] == 0)
    {
        pic[row, col] = 1;
        FillImage(row - 1, col);
        FillImage(row, col - 1);
        FillImage(row + 1, col);
        FillImage(row, col + 1);

    }
}

